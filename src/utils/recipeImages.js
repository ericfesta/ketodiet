/**
 * Get an image URL for a specific recipe by ID
 * @param {number} recipeId - The ID of the recipe
 * @returns {string} - URL of the recipe image
 */
export const getRecipeImageById = (recipeId) => {
  // If we have a specific image for this recipe ID, return it
  if (specificRecipeImages[recipeId]) {
    return specificRecipeImages[recipeId];
  }
  
  // Otherwise return a default image
  return "https://images.unsplash.com/photo-1546069901-ba9599a7e63c?ixlib=rb-4.0.3&ixid=M3wxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8fA%3D%3D&auto=format&fit=crop&w=1000&q=80";
};

/**
 * Get a random image URL for a recipe by meal type
 * @param {string} mealType - The type of meal (breakfast, lunch, dinner, snack, dessert)
 * @returns {string} - URL of a random recipe image for the meal type
 */
export const getRandomRecipeImageByType = (mealType) => {
  // Convert meal type to lowercase for consistency
  const type = mealType.toLowerCase();
  
  // If we have images for this meal type, return a random one
  if (recipeImageCollection[type] && recipeImageCollection[type].length > 0) {
    const randomIndex = Math.floor(Math.random() * recipeImageCollection[type].length);
    return recipeImageCollection[type][randomIndex];
  }
  
  // Otherwise return a default image
  return "https://images.unsplash.com/photo-1546069901-ba9599a7e63c?ixlib=rb-4.0.3&ixid=M3wxMjA3fDB8MHxwaG90by1wYWdlfHx8fGVufDB8fHx8fA%3D%3D&auto=format&fit=crop&w=1000&q=80";
};

/**
 * Get a random recipe image URL
 * @returns {string} - URL of a random recipe image
 */
export const getRandomRecipeImage = () => {
  // Get all meal types
  const mealTypes = Object.keys(recipeImageCollection);
  
  // Select a random meal type
  const randomMealType = mealTypes[Math.floor(Math.random() * mealTypes.length)];
  
  // Return a random image from that meal type
  return getRandomRecipeImageByType(randomMealType);
};

export default {
  getRecipeImageById,
  getRandomRecipeImageByType,
  getRandomRecipeImage
};
